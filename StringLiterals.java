public class StringLiterals {
    public static void main(String[] args) {
        /*
         * What is a string literal?
         * an exact sequence of characters (letters, numbers, or symbols) which are enclosed between two quotation marks
         * ex: "This is a string literal"
         */
        System.out.println("This is a string literal");
        System.out.println("ABC123$#%abc");
        System.out.println("Have a great day!!");
        System.out.println("AP CS A ROCKS!!");
    }
    
}
