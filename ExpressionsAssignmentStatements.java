public class ExpressionsAssignmentStatements {
    public static void main(String[] args) {
        System.out.println(3);
        System.out.println("3");
        System.out.println(3+3);
        System.out.println("3"+"3");

        /*
         * 1) Addition (8+4)
         * 2) Subtraction (8-4)
         * 3) Multiplication (8*4)
         * 4) Division (8/4)
         * 5) Modulus (8%4) -- Gets the remainder
         * 
         * int + int = int
         * double + double = double
         * int + double = double
         */
    }
    
}
