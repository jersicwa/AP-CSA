public class moreSwitch {
    public static void main(String[] args) {
        char selection = 'C';
        switch (selection) {
            case 'A':
                System.out.println("A");
                break;
            case 'B':
            System.out.println("B");
            break;
            case 'C':
                System.out.println("C");
            break;
        }
    }
}